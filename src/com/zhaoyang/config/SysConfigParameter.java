package com.zhaoyang.config;

public class SysConfigParameter {
	private String indexNewsIds;
	private String hotNewsIds;
	private String pageSize;
	public String getIndexNewsIds() {
		return indexNewsIds;
	}
	public void setIndexNewsIds(String indexNewsIds) {
		this.indexNewsIds = indexNewsIds;
	}
	public String getHotNewsIds() {
		return hotNewsIds;
	}
	public void setHotNewsIds(String hotNewsIds) {
		this.hotNewsIds = hotNewsIds;
	}
	public String getPageSize() {
		return pageSize;
	}
	public void setPageSize(String pageSize) {
		this.pageSize = pageSize;
	}

}
